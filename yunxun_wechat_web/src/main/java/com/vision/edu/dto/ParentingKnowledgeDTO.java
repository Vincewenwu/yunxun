package com.vision.edu.dto;

import java.io.Serializable;
import java.util.Date;

import com.baomidou.mybatisplus.annotations.TableField;

/**
*这是育儿知识的模型
*@author:zyml
*@date:2017年7月10日上午8:47:32
*/
public class ParentingKnowledgeDTO implements Serializable{

	private static final long serialVersionUID = 1L;
	
	/**
	 * 用户名称
	 */
	private String userName;
	
	/**
     * 标题
     */
	private String title;
    /**
     * 育儿知识名称
     */
	private String knowName;
    /**
     * 发布时间
     */
	@TableField("release_time")
	private Date releaseTime;
    /**
     * 图片
     */
	private String image;
    /**
     * url链接
     */
	private String url;
    /**
     * 内容
     */
	private String content;
	
	@TableField("disable_no")
	private Integer disableNo;
    /**
     * 浏览数
     */
	@TableField("browse_count")
	private Integer browseCount;
    /**
     * 点赞数
     */
	@TableField("point_count")
	private Integer pointCount;
    /**
     * 收藏数
     */
	@TableField("collection_num")
	private Integer collectionNum;
	
	 /**
     * 评论内容
     */
	private String comment;
    
	/**
	 * 学校名称
	 */
	private String schoolName;
	
	
	/**
     * 头像
     */
	@TableField("head_portrait")
	private String headPortrait;
	
	private Integer pCount;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getKnowName() {
		return knowName;
	}

	public void setKnowName(String knowName) {
		this.knowName = knowName;
	}

	public Date getReleaseTime() {
		return releaseTime;
	}

	public void setReleaseTime(Date releaseTime) {
		this.releaseTime = releaseTime;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getDisableNo() {
		return disableNo;
	}

	public void setDisableNo(Integer disableNo) {
		this.disableNo = disableNo;
	}

	public Integer getBrowseCount() {
		return browseCount;
	}

	public void setBrowseCount(Integer browseCount) {
		this.browseCount = browseCount;
	}

	public Integer getPointCount() {
		return pointCount;
	}

	public void setPointCount(Integer pointCount) {
		this.pointCount = pointCount;
	}

	public Integer getCollectionNum() {
		return collectionNum;
	}

	public void setCollectionNum(Integer collectionNum) {
		this.collectionNum = collectionNum;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getComment() {
		return comment;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getHeadPortrait() {
		return headPortrait;
	}

	public void setHeadPortrait(String headPortrait) {
		this.headPortrait = headPortrait;
	}

	public Integer getpCount() {
		return pCount;
	}

	public void setpCount(Integer pCount) {
		this.pCount = pCount;
	}
	
	
	

}
